#pragma once
#include "pch.h"
#include <iostream>
#include "MonsterSpawnInfoManagerTemplate.h"


////////////////////////////////////////////////////////////////////////////////////////////////////
// @brief GetInfo
////////////////////////////////////////////////////////////////////////////////////////////////////
const MonsterSpawnInfo* MonsterSpawnInfoManagerTemplate::GetInfo( AtInt32 id )
{
	auto iter = m_infoMap.find( id );
	if ( iter == m_infoMap.end() )
		return nullptr;

	return &(iter->second);
}

////////////////////////////////////////////////////////////////////////////////////////////////////
// @brief AddInfo
////////////////////////////////////////////////////////////////////////////////////////////////////
AtBool MonsterSpawnInfoManagerTemplate::_AddInfo(
	    AtInt32 id,
	    AtInt32 monsterInfoId,
	    AtInt32 aIInfoId )
{
	auto iter = m_infoMap.find( id );
	if ( iter != m_infoMap.end() )
		return false;

	MonsterSpawnInfo info = MonsterSpawnInfo(
	    id,
	    monsterInfoId,
	    aIInfoId );

	m_infoMap[ id ] = info;

	return true;
}

////////////////////////////////////////////////////////////////////////////////////////////////////
// @brief Initialize
////////////////////////////////////////////////////////////////////////////////////////////////////
AtBool MonsterSpawnInfoManagerTemplate::_Initialize()
{
	INFO_LOG( "MonsterSpawnInfoManager Initialize()" );
	if ( !_AddInfo( 1, 1001, 101 ) ) return false;
	if ( !_AddInfo( 2, 1002, 102 ) ) return false;
	if ( !_AddInfo( 3, 1003, 103 ) ) return false;

	return true;
}