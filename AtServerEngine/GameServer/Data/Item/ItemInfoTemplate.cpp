////////////////////////////////////////////////////////////////////////////////////////////////////
// @brief ItemInfoTemplate class
////////////////////////////////////////////////////////////////////////////////////////////////////


#pragma once
#include "pch.h"
#include "ItemInfoTemplate.h"


////////////////////////////////////////////////////////////////////////////////////////////////////
// @brief Constructor
////////////////////////////////////////////////////////////////////////////////////////////////////
ItemInfoTemplate::ItemInfoTemplate()
{
	m_id = AtInt32( 0 );
	m_classType = Protocol::EPlayerType( 0 );
	m_name = "";
	m_level = AtInt32( 0 );
	m_equipSlotType = Protocol::EEquipSlotType( 0 );
	m_stat = Protocol::EStat( 0 );
	m_statParam = AtInt32( 0 );
}

////////////////////////////////////////////////////////////////////////////////////////////////////
// @brief Constructor
////////////////////////////////////////////////////////////////////////////////////////////////////
ItemInfoTemplate::ItemInfoTemplate(
	AtInt32 id,
	Protocol::EPlayerType classType,
	AtString name,
	AtInt32 level,
	Protocol::EEquipSlotType equipSlotType,
	Protocol::EStat stat,
	AtInt32 statParam )
{
	m_id = id;
	m_classType = classType;
	m_name = name;
	m_level = level;
	m_equipSlotType = equipSlotType;
	m_stat = stat;
	m_statParam = statParam;
}

////////////////////////////////////////////////////////////////////////////////////////////////////
// @brief Destructor
////////////////////////////////////////////////////////////////////////////////////////////////////
ItemInfoTemplate::~ItemInfoTemplate()
{
}