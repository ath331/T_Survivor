#include "pch.h"
#include "AICondition.h"


////////////////////////////////////////////////////////////////////////////////////////////////////
// @breif 생성자
////////////////////////////////////////////////////////////////////////////////////////////////////
AICondition::AICondition( ConditionFunc conditionFunc )
	: 
	m_conditionFunc( conditionFunc )
{
}

////////////////////////////////////////////////////////////////////////////////////////////////////
// @breif 실행한다
////////////////////////////////////////////////////////////////////////////////////////////////////
AIStatus AICondition::Execute( Actor* actor )
{
	if ( m_conditionFunc( actor ) )
	{
		std::cout << "조건 만족\n";
		return AIStatus::Success;
	}
	else
	{
		std::cout << "조건 불만족\n";
		return AIStatus::Failure;
	}
}
