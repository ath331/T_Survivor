////////////////////////////////////////////////////////////////////////////////////////////////////
// @breif 생성자
////////////////////////////////////////////////////////////////////////////////////////////////////
PlayRoom::PlayRoom()
{
	// TODO : 임시 데이터 그룹을 사용중
	AtInt32 spawnGroupId = 100;
	m_monsterSpawnManager = new MonsterSpawnManager( this, spawnGroupId );
}

////////////////////////////////////////////////////////////////////////////////////////////////////
// @breif 소멸자
////////////////////////////////////////////////////////////////////////////////////////////////////
PlayRoom::~PlayRoom()
{
	if ( m_monsterSpawnManager )
		delete m_monsterSpawnManager;
}

////////////////////////////////////////////////////////////////////////////////////////////////////
// @breif 룸을 업데이트한다.
////////////////////////////////////////////////////////////////////////////////////////////////////
AtVoid PlayRoom::UpdateTick()
{
	if ( m_monsterSpawnManager )
		m_monsterSpawnManager->Update();

	Room::UpdateTick();
}
