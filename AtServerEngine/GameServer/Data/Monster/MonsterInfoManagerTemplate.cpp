////////////////////////////////////////////////////////////////////////////////////////////////////
// @brief GetInfo
////////////////////////////////////////////////////////////////////////////////////////////////////
const MonsterInfo* MonsterInfoManagerTemplate::GetInfo( AtInt32 id )
{
	auto iter = m_infoMap.find( id );
	if ( iter == m_infoMap.end() )
		return nullptr;

	return &(iter->second);
}

////////////////////////////////////////////////////////////////////////////////////////////////////
// @brief AddInfo
////////////////////////////////////////////////////////////////////////////////////////////////////
AtBool MonsterInfoManagerTemplate::_AddInfo(
	    AtInt32 id,
	    AtString string,
	    AtInt32 defaultHP,
	    AtInt32 defaultAttack )
{
	auto iter = m_infoMap.find( id );
	if ( iter != m_infoMap.end() )
		return false;

	MonsterInfo info = MonsterInfo(
	    id,
	    string,
	    defaultHP,
	    defaultAttack );

	m_infoMap[ id ] = info;

	return true;
}

