#include "pch.h"
#include "Monster.h"
#include "Logic/AI/AI.h"


////////////////////////////////////////////////////////////////////////////////////////////////////
// @breif 생성자
////////////////////////////////////////////////////////////////////////////////////////////////////
Monster::Monster()
{
	m_actorType = Protocol::EActorType::ACTOR_TYPE_MONSTER;
	m_ai = new AI();
}

////////////////////////////////////////////////////////////////////////////////////////////////////
// @breif 소멸자
////////////////////////////////////////////////////////////////////////////////////////////////////
Monster::~Monster()
{
	if ( m_ai )
		delete m_ai;
}

////////////////////////////////////////////////////////////////////////////////////////////////////
// @breif 업데이트
////////////////////////////////////////////////////////////////////////////////////////////////////
AtVoid Monster::Update()
{
	if ( m_ai )
		m_ai->Update( this );
}
