////////////////////////////////////////////////////////////////////////////////////////////////////
// @brief ClassInfoTemplate class
////////////////////////////////////////////////////////////////////////////////////////////////////


#pragma once
#include "pch.h"
#include "ClassInfoTemplate.h"


////////////////////////////////////////////////////////////////////////////////////////////////////
// @brief Constructor
////////////////////////////////////////////////////////////////////////////////////////////////////
ClassInfoTemplate::ClassInfoTemplate()
{
	m_id = Protocol::EPlayerType( 0 );
	m_hP = AtInt32( 0 );
	m_mP = AtInt32( 0 );
	m_damage = AtInt32( 0 );
	m_magicDamage = AtInt32( 0 );
}

////////////////////////////////////////////////////////////////////////////////////////////////////
// @brief Constructor
////////////////////////////////////////////////////////////////////////////////////////////////////
ClassInfoTemplate::ClassInfoTemplate(
	Protocol::EPlayerType id,
	AtInt32 hP,
	AtInt32 mP,
	AtInt32 damage,
	AtInt32 magicDamage )
{
	m_id = id;
	m_hP = hP;
	m_mP = mP;
	m_damage = damage;
	m_magicDamage = magicDamage;
}

////////////////////////////////////////////////////////////////////////////////////////////////////
// @brief Destructor
////////////////////////////////////////////////////////////////////////////////////////////////////
ClassInfoTemplate::~ClassInfoTemplate()
{
}