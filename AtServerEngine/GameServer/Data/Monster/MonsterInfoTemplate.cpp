////////////////////////////////////////////////////////////////////////////////////////////////////
// @brief MonsterInfoTemplate class
////////////////////////////////////////////////////////////////////////////////////////////////////


#pragma once
#include "pch.h"
#include "MonsterInfoTemplate.h"


////////////////////////////////////////////////////////////////////////////////////////////////////
// @brief Constructor
////////////////////////////////////////////////////////////////////////////////////////////////////
MonsterInfoTemplate::MonsterInfoTemplate()
{
	m_id = AtInt32( 0 );
	m_string = "";
	m_defaultHP = AtInt32( 0 );
	m_defaultAttack = AtInt32( 0 );
}

////////////////////////////////////////////////////////////////////////////////////////////////////
// @brief Constructor
////////////////////////////////////////////////////////////////////////////////////////////////////
MonsterInfoTemplate::MonsterInfoTemplate(
	AtInt32 id,
	AtString string,
	AtInt32 defaultHP,
	AtInt32 defaultAttack )
{
	m_id = id;
	m_string = string;
	m_defaultHP = defaultHP;
	m_defaultAttack = defaultAttack;
}

////////////////////////////////////////////////////////////////////////////////////////////////////
// @brief Destructor
////////////////////////////////////////////////////////////////////////////////////////////////////
MonsterInfoTemplate::~MonsterInfoTemplate()
{
}