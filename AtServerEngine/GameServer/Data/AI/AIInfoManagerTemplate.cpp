#pragma once
#include "pch.h"
#include <iostream>
#include "AIInfoManagerTemplate.h"


////////////////////////////////////////////////////////////////////////////////////////////////////
// @brief GetInfo
////////////////////////////////////////////////////////////////////////////////////////////////////
const AIInfo* AIInfoManagerTemplate::GetInfo( AtInt32 id )
{
	auto iter = m_infoMap.find( id );
	if ( iter == m_infoMap.end() )
		return nullptr;

	return &(iter->second);
}

////////////////////////////////////////////////////////////////////////////////////////////////////
// @brief AddInfo
////////////////////////////////////////////////////////////////////////////////////////////////////
AtBool AIInfoManagerTemplate::_AddInfo(
	    AtInt32 id )
{
	auto iter = m_infoMap.find( id );
	if ( iter != m_infoMap.end() )
		return false;

	AIInfo info = AIInfo(
	    id );

	m_infoMap[ id ] = info;

	return true;
}

////////////////////////////////////////////////////////////////////////////////////////////////////
// @brief Initialize
////////////////////////////////////////////////////////////////////////////////////////////////////
AtBool AIInfoManagerTemplate::_Initialize()
{
	INFO_LOG( "AIInfoManager Initialize()" );
	if ( !_AddInfo( 101 ) ) return false;
	if ( !_AddInfo( 102 ) ) return false;
	if ( !_AddInfo( 103 ) ) return false;

	return true;
}