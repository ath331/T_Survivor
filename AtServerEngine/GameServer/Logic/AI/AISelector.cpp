#include "pch.h"
#include "AISelector.h"


////////////////////////////////////////////////////////////////////////////////////////////////////
// @breif 생성자
////////////////////////////////////////////////////////////////////////////////////////////////////
AISelector::AISelector( const std::vector< AINode* >& nodes )
	:
	children( nodes )
{
}

////////////////////////////////////////////////////////////////////////////////////////////////////
// @breif 소멸자
////////////////////////////////////////////////////////////////////////////////////////////////////
AISelector::~AISelector()
{
	for ( auto child : children )
	{
		if ( child )
			delete child;
	}
}

////////////////////////////////////////////////////////////////////////////////////////////////////
// @breif 실행한다
////////////////////////////////////////////////////////////////////////////////////////////////////
AIStatus AISelector::Execute( Actor* actor )
{
	for ( auto child : children )
	{
		AIStatus status = child->Execute( actor );
		if ( status == AIStatus::Success )
		{
			return AIStatus::Success;
		}
	}

	return AIStatus::Failure;
}
