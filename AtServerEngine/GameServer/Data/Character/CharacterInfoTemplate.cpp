#pragma once
#include "pch.h"
#include "CharacterInfoTemplate.h"


////////////////////////////////////////////////////////////////////////////////////////////////////
// @brief Constructor
////////////////////////////////////////////////////////////////////////////////////////////////////
CharacterInfoTemplate::CharacterInfoTemplate()
{
	m_id = AtInt32( 0 );
	m_isNonUse = AtBool( 0 );
	m_bagType = Protocol::EBagType( 0 );
}

////////////////////////////////////////////////////////////////////////////////////////////////////
// @brief Constructor
////////////////////////////////////////////////////////////////////////////////////////////////////
CharacterInfoTemplate::CharacterInfoTemplate(
	AtInt32 id,
	AtBool isNonUse,
	Protocol::EBagType bagType )
{
	m_id = id;
	m_isNonUse = isNonUse;
	m_bagType = bagType;
}

